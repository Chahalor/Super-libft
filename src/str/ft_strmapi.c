#include "libft.h"

/**
 * @brief Applies the function f to each character of the string passed as
 * argument by giving its index as first argument to create a new string
 * resulting from successive applications of f.
 * 
 * @param s String to iterate.
 * @param f Function to apply to each character.
 * @return char* The new string. NULL if the allocation fails.
 */
char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*result;

	if (!s || !f)
		return (NULL);
	result = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!result)
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
